package com.pa.chen.apprxandroid;

public class RxMediaValue {
    private String code;
    public RxMediaValue() {
    }
    public RxMediaValue(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
